# Constant Expressions
x = 15 + 1.3

print(x)
# Arithmetic Expressions
x = 40
y = 12

add = x + y
sub = x - y
pro = x * y
div = x / y

print(add)
print(sub)
print(pro)
print(div)

# Integral Expressions
a = 13
b = 12.0

c = a + int(b)
print(c)

# Floating Expressions
a = 13
b = 5

c = a / b
print(c)

# Relational Expressions
a = 21
b = 13
c = 40
d = 37

p = (a + b) >= (c - d)
print(p)

P = (10 == 9)
Q = (7 > 5)

# Logical Expressions
R = P and Q
S = P or Q
T = not P

print(R)
print(S)
print(T)
# Bitwise Expressions
a = 12

x = a >> 2
y = a << 1

print(x, y)
# Combinational Expressions
a = 16
b = 12

c = a + (b >> 1)
print(c)
# Multi-operator expression

a = 10 + 3 * 4
print(a)

b = (10 + 3) * 4
print(b)

c = 10 + (3 * 4)
print(c)
